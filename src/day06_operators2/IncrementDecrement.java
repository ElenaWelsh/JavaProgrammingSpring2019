package day06_operators2;

public class IncrementDecrement {
	public static void main(String[] args) {
		int employees = 57;
		employees ++; // add 1
		employees +=1; //
		employees = employees + 1;
		String s = "Employees ";
		
		System.out.println(s + employees);
		employees --; // - 1 Decrease
		-- employees;
		System.out.println(employees);	
			
		
	}

}
