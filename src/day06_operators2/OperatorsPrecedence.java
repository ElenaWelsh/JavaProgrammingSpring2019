package day06_operators2;

public class OperatorsPrecedence {
	public static void main(String[] args) {
		
		System.out.println("Hello" + 6 + 10);
		System.out.println("Hello + 6"+ 10);
		System.out.println(6 + 10 + "Hello");
		System.out.println("Hello"+ (6+10));
		System.out.println("Hello" + 6 / 10);
		int c = 5/2;
		System.out.println(c);
	    float f = 4.0f;
		System.out.println(f);
		
	
	}

}
